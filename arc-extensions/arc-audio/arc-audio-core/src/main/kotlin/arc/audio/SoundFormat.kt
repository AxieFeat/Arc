package arc.audio

/**
 * This enum represents some file formats of sounds.
 */
enum class SoundFormat {

    /**
     * `.ogg` sounds.
     */
    OGG,

    /**
     * `.wav` sounds.
     */
    WAV,

    /**
     * `.mp3` sounds.
     */
    MP3,

    /**
     * `.flac` sounds.
     */
    FLAC

}